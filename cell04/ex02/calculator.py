#!/usr/bin/env python3

# Solicitar o primeiro número ao usuário
num1 = float(input("Give me the first number: "))

# Solicitar o segundo número ao usuário
num2 = float(input("Give me the second number: "))

# Exibir uma mensagem de agradecimento
print("Thank you!")

# Exibir os resultados das operações matemáticas
print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} / {num2} = {num1 / num2}")
print(f"{num1} * {num2} = {num1 * num2}")
